package Model;

public class CrdGenInfo {
    private String cgcgen;
    private String cgdesc;
    private String cgufnr;
    

	public CrdGenInfo() {
		this.cgcgen = null;
		this.cgdesc = null;
		this.cgufnr = null;
	}
	
	public String getCgcgen() {
		return cgcgen;
	}
	public void setCgcgen(String cgcgen) {
		this.cgcgen = cgcgen;
	}
	public String getCgdesc() {
		return cgdesc;
	}
	public void setCgdesc(String cgdesc) {
		this.cgdesc = cgdesc;
	}
	public String getCgufnr() {
		return cgufnr;
	}
	public void setCgufnr(String cgufnr) {
		this.cgufnr = cgufnr;
	}
    

}
